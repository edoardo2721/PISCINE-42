int	ft_sqrt(int nb)
{
	int		f;
	int		l;
	long	i;

	f = 0;
	l = nb;
	while (f <= l)
	{
		i = (f + l) / 2;
		if (i * i == nb)
			return ((int)i);
		if (i * i < nb)
			f = i + 1;
		else
			l = i - 1;
	}
	return (0);
}
